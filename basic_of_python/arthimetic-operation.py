import math
x = 10
# addition
print(x+2)
# subtraction
print(x-2)
# multiplication
print(x*2)
# division
print(x/2)  # give float quotient 5.0
print(x//2)  # give int quotient s
# modulus
print(x % 3)
# power
print(x**2)
# operator precedences
y = 10+3*2
print(y)  # it gives 16
y = 10+3*2**2
print(y)  # gives 22
# mathematics functions
a = 2.9
b = -3.6
# rounding numbers
print(round(a))  # gives 3
print(math.ceil(a))  # gives 3
print(math.floor(a))  # gives 2
# absolute values for number
print(abs(b))  # gives 3.6
